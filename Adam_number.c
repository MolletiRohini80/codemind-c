#include<stdio.h>
int main()
{
int n,s=0,s1=0,q,sq,r,n1,q1,sq1,r1,rsq;
scanf("%d",&n);
q=n;
while(q!=0)
{
  r=q%10;
  s=s*10+r;
  q=q/10;
}
n1=s;
sq=n*n;
sq1=n1*n1;
q1=sq1;
while(q1!=0)
{
  r1=q1%10;
  s1=s1*10+r1;
  q1=q1/10;
}
 rsq=s1;
 if(sq==rsq)
 {
  printf("True");
 }
 else
  {
  printf("False");
  }
}